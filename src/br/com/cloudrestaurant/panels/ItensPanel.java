    public void preencheTabelaItens(String sql){
        ArrayList dados = new ArrayList();
        String[] nomeColunas = new String[]{"ITEM","VALOR VENDA"};
//        Metodos metodo = new Metodos();
//        metodo.salvarTexto(sql);
        try{
            con = new Conexao();
            Connection conn = con.getConnection();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            rs.first();
            
            if (rs.absolute(1)){
                do{
                    dados.add(new Object[]{rs.getString("nome"), rs.getString("valorVenda")});
                }while(rs.next());
            }else{
                labelMensagem.setVisible(true);
                labelMensagem.setText("Não contém dados na tabela. Insira um novo.");
            }
            
        ModeloTabela modelo = new ModeloTabela(dados, nomeColunas);
        tabelaItens.setModel(modelo);
        tabelaItens.getColumnModel().getColumn(0).setPreferredWidth(180); // define o tamanho da coluna.
        tabelaItens.getColumnModel().getColumn(0).setResizable(false);// Não poderá alterar o tamanho.
        tabelaItens.getColumnModel().getColumn(1).setPreferredWidth(100); // define o tamanho da coluna.
        tabelaItens.getColumnModel().getColumn(1).setResizable(false);// Não poderá alterar o tamanho.
        
        tabelaItens.getTableHeader().setReorderingAllowed(false);
        tabelaItens.setAutoResizeMode(tabelaItens.AUTO_RESIZE_OFF);
        
        tabelaItens.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            
        }catch(IOException | SQLException ex){
            JOptionPane.showMessageDialog(null, "Problemas. Erro: " + ex.getMessage());
            labelMensagem.setVisible(true);
//            labelMensagem.setText("Não há registro na tabela. Inclua um novo.");
        }
        
    }
    
    
