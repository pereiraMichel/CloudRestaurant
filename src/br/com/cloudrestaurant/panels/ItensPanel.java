    public void preencheDadosItem(){
        
        try{
            String sql = "SELECT i.*, f.nome, f.idFornecedor "
                    + "FROM itens i "
                    + "INNER JOIN fornecedores f ON i.codFornecedor = f.idFornecedor "
                    + "WHERE i.nome = '" + textItem.getText() + "'";
            con = new Conexao();
            conn = con.getConnection();
            stmt = conn.createStatement();
            rs = stmt.executeQuery(sql);
            rs.next();
            
            textFornecedor.setText(rs.getString("f.nome"));
            textDesconto.setText(rs.getString("i.desconto"));
            textAcrescimo.setText(rs.getString("i.acrescimo"));
            textValorVenda.setText(rs.getString("i.valorVenda"));
            textValorCusto.setText(rs.getString("i.valorCusto"));
            textQuantidade.setText(rs.getString("i.quantidade"));
            textIdItem.setText(rs.getString("i.idItem"));
            textIdFornecedor.setText(rs.getString("f.idFornecedor"));
            
            
        }catch(IOException | SQLException ex){
            labelMensagem.setVisible(true);
            labelMensagem.setText("Não foi possível realizar a consulta. Erro: " + ex.getMessage());
        }
        
    }
    
